#len()
string_length = len("Hello World!")
list_length = len([1, 2, 3, 4, 5])
tuple_length = len((10, 20, 30))

print(string_length)
print(list_length)
print(tuple_length)

#sum()
sum_of_list = sum([1, 2, 3, 4, 5])
sum_of_tuple = sum((10, 5, 10))

print(sum_of_list)
print(sum_of_tuple)

#max()
max_value = max(5, 10, 3)
max_float = max(3.14, 2.71, 5.0)

print(max_value)
print(max_float)